def display_dictionary(address_book):
    # Проверяем, есть ли записи в адресной книге
    if address_book:
        print("\nСловарь адресов:")
        # Проходим по всем записям в словаре и выводим имя и адрес
        for name, address in address_book.items():
            print(f"{name}: {address}")
    else:
        # Если адресная книга пуста, сообщаем об этом
        print("\nСловарь пуст.")

def main():
    # Инициализируем пустую адресную книгу в виде словаря
    address_book = {}

    # Запускаем бесконечный цикл для взаимодействия с пользователем
    while True:
        print("\nВыберите действие:")
        print("1. Добавить человека")
        print("2. Изменить адрес")
        print("3. Удалить запись")
        print("4. Просмотреть весь словарь")
        print("5. Выход")

        # Запрашиваем у пользователя выбор действия
        choice = input("Введите номер действия: ")

        if choice == '1':
            # Добавление нового человека в адресную книгу
            name = input("Введите имя: ")
            address = input("Введите адрес: ")
            address_book[name] = address  # Сохраняем имя и адрес в словаре
            print(f"Добавлено: {name} - {address}")

        elif choice == '2':
            # Изменение адреса существующего человека
            name = input("Введите имя человека, адрес которого хотите изменить: ")
            if name in address_book:  # Проверяем, есть ли запись
                new_address = input("Введите новый адрес: ")
                address_book[name] = new_address  # Обновляем адрес
                print(f"Адрес для {name} изменён на: {new_address}")
            else:
                # Если записи нет, сообщаем об этом
                print(f"Человек с именем {name} не найден.")

        elif choice == '3':
            # Удаление записи о человеке
            name = input("Введите имя человека, запись которого хотите удалить: ")
            if name in address_book:  # Проверяем, есть ли запись
                del address_book[name]  # Удаляем запись
                print(f"Запись для {name} удалена.")
            else:
                # Если записи нет, сообщаем об этом
                print(f"Человек с именем {name} не найден.")

        elif choice == '4':
            # Просмотр всего словаря
            display_dictionary(address_book)

        elif choice == '5':
            # Выход из программы
            print("Выход из программы.")
            break

        else:
            # Если введено некорректное действие, сообщаем об этом
            print("Некорректный ввод. Пожалуйста, выберите правильное действие.")

# Запускаем основную функцию при выполнении скрипта
if __name__ == "__main__":
    main()